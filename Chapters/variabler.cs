Variabler har et gyldighedsområde (scope) og skal have unikke navne.

Konstanter er værdier, der ikke kan ændres efter initialisering, deklareres med ""const"" nøgleordet.

Variabler er afgørende for at gemme og arbejde med data i C#-programmer.";

